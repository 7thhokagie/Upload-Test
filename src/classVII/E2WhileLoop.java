package classVII;

public class E2WhileLoop {
    public static void main(String[] args) {

        int number=0;
        // ++ adds 1
        number++; // 0+1=1
        number++; // 1+1=2
        System.out.println(number);

        int num2=10;
        num2--;
        System.out.println(num2);

    }
}
